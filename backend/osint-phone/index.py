def handler(event: dict, context) -> dict:
    """Анализ номера телефона: оператор и регион по коду DEF"""

    if event.get('httpMethod') == 'OPTIONS':
        return {'statusCode': 200, 'headers': {'Access-Control-Allow-Origin': '*', 'Access-Control-Allow-Methods': 'GET, POST, OPTIONS', 'Access-Control-Allow-Headers': 'Content-Type'}, 'body': ''}

    body = json.loads(event.get('body') or '{}')
    phone = body.get('query', '').strip()

    if not phone:
        return {'statusCode': 400, 'headers': {'Access-Control-Allow-Origin': '*'}, 'body': json.dumps({'error': 'Введите номер телефона'})}

    # Очищаем и нормализуем
    digits = re.sub(r'\D', '', phone)
    if digits.startswith('8') and len(digits) == 11:
        digits = '7' + digits[1:]

    if len(digits) < 10:
        return {'statusCode': 400, 'headers': {'Access-Control-Allow-Origin': '*'}, 'body': json.dumps({'error': 'Неверный формат номера телефона'})}

    # Берём 10 последних цифр (без кода страны)
    local = digits[-10:]
    prefix3 = local[:3]

    # Форматируем красиво
    formatted = f'+7 ({local[:3]}) {local[3:6]}-{local[6:8]}-{local[8:10]}'

    carrier = CARRIER_MAP.get(prefix3)
    region = REGION_MAP.get(prefix3)
    country = 'Россия' if digits.startswith('7') else 'Неизвестно'

    result = {
        'phone': formatted,
        'country': country,
        'carrier': carrier or 'Неизвестный оператор',
        'region': region or 'Регион не определён',
        'type': 'Мобильный',
        'qc': 0 if carrier else 1,
    }

    return {
        'statusCode': 200,
        'headers': {'Access-Control-Allow-Origin': '*'},
        'body': json.dumps({'result': result}, ensure_ascii=False)
    }
